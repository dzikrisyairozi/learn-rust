// This is a simple "Hello, Rust!" program that demonstrates basic Rust syntax
//
// The main() function is the entry point of every Rust program
// It takes no arguments and returns no value (implicit unit type '()')
//
// The println! macro is used to print text to the console
// The '!' indicates that println is a macro rather than a regular function
fn main() {
    println!("Hello, Rust!");
}
